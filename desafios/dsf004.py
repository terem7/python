n = input('Digite algo: ')

print(type(n))
print('Verifica se todos os caracteres da string são dígitos decimais (0–9).',n.isdecimal())
print(' Verifica se todos os caracteres da string são letras (A-Z ou a-z).',n.isalpha())
print('Verifica se todos os caracteres da string são espaços em branco (espaço, tabulação, etc.).',n.isspace())